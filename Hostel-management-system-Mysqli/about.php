<h2><b>About institute</b></h2>
<p>
Shah and Anchor Kutchhi Engineering College was established in 1985 for the purpose of imparting quality technical education. The college is managed by Mahavir Education Trust. The college is approved by AICTE, New Delhi and Government of Maharashtra, and is affiliated to University of Mumbai. It also has an ISO -9001 Certification. It offers under graduate courses in Computer Engineering, Information Technology, Electronics Engineering and Electronics & Telecommunication Engineering. It has started an extra division in Computer Engineering for Diploma Students. It also offers Post Graduate courses in Computer Engineering, Information Technology, Electronics Engineering.
UG Programs in Computer Engineering and Information Technology of the college have been awarded accreditation by National Board of Accreditation (NBA) from A.Y. 2019-20 for 3 Years.</p>
